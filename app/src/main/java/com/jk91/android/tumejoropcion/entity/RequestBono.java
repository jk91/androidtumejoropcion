public class RequestBono {

    private String googleId;
    private String valor;
    private String loginTienda;
    private String idCuentaUsuario;
    private String tipoUsuario;
    private String idAmigo;
    private String tipoAmigo;

    public String getGoogleId() {
        return googleId;
    }

    public void setGoogleId(String googleId) {
        this.googleId = googleId;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getLoginTienda() {
        return loginTienda;
    }

    public void setLoginTienda(String loginTienda) {
        this.loginTienda = loginTienda;
    }

    public String getIdCuentaUsuario() {
        return idCuentaUsuario;
    }

    public void setIdCuentaUsuario(String idCuentaUsuario) {
        this.idCuentaUsuario = idCuentaUsuario;
    }

    public String getTipoUsuario() {
        return tipoUsuario;
    }

    public void setTipoUsuario(String tipoUsuario) {
        this.tipoUsuario = tipoUsuario;
    }

    public String getIdAmigo() {
        return idAmigo;
    }

    public void setIdAmigo(String idAmigo) {
        this.idAmigo = idAmigo;
    }

    public String getTipoAmigo() {
        return tipoAmigo;
    }

    public void setTipoAmigo(String tipoAmigo) {
        this.tipoAmigo = tipoAmigo;
    }
}
